#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		size;
	int		i;
	char	*dest;

	size = ft_strlen(s1) + 1;
	dest = NULL;
	dest = (char *)g_malloc(sizeof(char) * size);
	if (!dest)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		dest[i] = s1[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
